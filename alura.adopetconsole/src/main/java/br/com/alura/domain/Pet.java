package br.com.alura.domain;

public class Pet {

	public Pet(String tipo, String nome, String raca, String idade, String cor, String peso) {
		this.tipo = tipo;
		this.nome = nome;
		this.raca = raca;
		this.idade = idade;
		this.cor = cor;
		this.peso = peso;
	}

	private String tipo;
	private String nome;
	private String raca;
	private String idade;
	private String cor;
	private String peso;

}
